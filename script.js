function countColumnsFilled(){
	var Name = parseFloat(document.getElementById('Name').value);
	
	var check = 0;
	var total = 0;
	if(Name > 0 && Name < 100 && Name !== 'NaN') {
		check += 1;
		total += Name;
	}
	
	return [check, total];
}
  
